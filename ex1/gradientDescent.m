function [theta, J_history] = gradientDescent(X, y, theta, alpha, num_iters)
%GRADIENTDESCENT Performs gradient descent to learn theta
%   theta = GRADIENTDESCENT(X, y, theta, alpha, num_iters) updates theta by 
%   taking num_iters gradient steps with learning rate alpha

% Initialize some useful values
m = length(y); % number of training examples
J_history = zeros(num_iters, 1);
temp = zeros(2,1);
% update=zeros(2,1);
for iter = 1:num_iters   %num_iters次迭代

    % ====================== YOUR CODE HERE ======================
    % Instructions: Perform a single gradient step on the parameter vector
    %               theta. 
    %
    % Hint: While debugging, it can be useful to print out the values
    %       of the cost function (computeCost) and gradient here.
    fprintf(' 第 %d 次 iteration   ',iter);
    
        temp(1,1) = theta(1,1) - alpha/m*sum(X*theta - y);
        temp(2,1) = theta(2,1) - alpha/m*sum((X*theta-y) .* X(:,2));
        theta = temp;
   
%    这种用循环实现是不对的，因为每一个i值，对应等号右边的update值都是不同的，而
%    对于迭代的公式  theta(0) = theta(0) - alpha/m*Σ(i=1:m)(X*theta - y)来说，
%    在计算从1到m的求和时，theta(0)是没有改变的，所以不能用这种求和实现
%      for i=1:m
% 
% 		update=update+(X(i,:)*theta-y(i))*X(i,:)';    
% 
%     end
% 
%     update=update*alpha/(m);
% 
%     theta=theta-update; 
%     
%    


    % ============================================================

    % Save the cost J in every iteration    
    J_history(iter) = computeCost(X, y, theta);
   

end

end
